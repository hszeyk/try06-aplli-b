問題①
・定数nameを作り、自分の名前を代入しよう
・定数name2を作り、自分以外の名前を代入しよう
・if文を使って、nameとname2が一致したら「ようこそ」、しなかったら「入れません」と出力するプログラムを書こう

----------------------------------*/

void main() {
  var birth = [2, 3, 10];

  if (birth[0] == 2) {
    print("おめでとう");
  } else {
    print("こんにちわ");
  }

  if (birth[1] == 2) {
    print("おめでとう");
  } else {
    print("こんにちわ");
  }
}
